package com.queueusinglinkedlist;

public class QueueNode {
	
	int data;
	QueueNode next;
	
	
	public QueueNode() {
	}


	public int getData() {
		return data;
	}


	public void setData(int data) {
		this.data = data;
	}


	public QueueNode getNext() {
		return next;
	}


	public void setNext(QueueNode next) {
		this.next = next;
	}
	
	
}
